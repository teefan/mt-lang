    def cache_command
      subcommand = @argv.shift
      unless subcommand
        @err.puts("missing cache subcommand")
        print_command_help("cache", @err)
        return 1
      end

      cache_root = MilkTea.data_root.join("tmp", "mtc-cache")

      case subcommand
      when "purge"
        if File.directory?(cache_root)
          FileUtils.rm_rf(cache_root)
          @out.puts("purged #{cache_root}")
        else
          @out.puts("cache is already empty")
        end
        0
      when "status"
        unless File.directory?(cache_root)
          @out.puts("cache directory does not exist: #{cache_root}")
          return 0
        end
        program_dirs = Dir.glob(File.join(cache_root, "programs", "*", "*")).select { |d| File.directory?(d) }
        binary_files = Dir.glob(File.join(cache_root, "binaries", "*", "*", "binary")).select { |f| File.file?(f) }
        total_size = (program_dirs + binary_files).sum { |p|
          File.file?(p) ? File.size(p) : Dir.glob(File.join(p, "**", "*")).sum { |f| File.file?(f) ? File.size(f) : 0 }
        }
        @out.puts("cache root   #{cache_root}")
        @out.puts("cached programs  #{program_dirs.length}")
        @out.puts("cached binaries  #{binary_files.length}")
        @out.puts("total size       #{format_size(total_size)}")
        0
      else
        @err.puts("unknown cache subcommand #{subcommand}")
        print_command_help("cache", @err)
        1
      end
    end

    def parse_build_options(allow_clean: false)
      options = {
        output_path: nil,
        cc: ENV.fetch("CC", "cc"),
        keep_c_path: nil,
        profile: nil,
        platform: nil,
        bundle: false,
        archive: false,
        locked: false,
        frozen: false,
      }
      options[:clean] = false if allow_clean

      until @argv.empty?
        option = @argv.shift
        case option
        when "-o", "--output"
          value = @argv.shift
          return missing_option_value(option) unless value

          options[:output_path] = value
        when "--cc"
          value = @argv.shift
          return missing_option_value(option) unless value

          options[:cc] = value
        when "--keep-c"
          value = @argv.shift
          return missing_option_value(option) unless value

          options[:keep_c_path] = value
        when "--profile"
          value = @argv.shift
          return missing_option_value(option) unless value

          options[:profile] = value
        when "--platform"
          value = @argv.shift
          return missing_option_value(option) unless value

          options[:platform] = value
        when "--bundle"
          options[:bundle] = true
        when "--archive"
          options[:bundle] = true
          options[:archive] = true
        when "--locked"
          options[:locked] = true
        when "--frozen"
          options[:locked] = true
          options[:frozen] = true
        when "--clean"
          if allow_clean
            options[:clean] = true
          else
            @err.puts("unknown build option #{option}")
            print_usage(@err)
            return nil
          end
        when "--"
          break
        else
          if option.start_with?("-")
            @err.puts("unknown build option #{option}")
            print_usage(@err)
            return nil
          end
          @argv.unshift(option)
          break
        end
      end

      options
    end

    def parse_format_options
      options = {
        check: false,
        write: false,
        mode: :safe,
        max_line_length: nil,
        profile: false,
      }
      input_paths = []

      until @argv.empty?
        option = @argv.shift
        if option.start_with?("-")
          case option
          when "--check"
            options[:check] = true
          when "--write", "-w"
            options[:write] = true
          when "--preserve"
            options[:mode] = :preserve
          when "--canonical"
            options[:mode] = :canonical
          when "--safe"
            options[:mode] = :safe
          when "--tidy"
            options[:mode] = :tidy
          when "--max-line-length"
            value = @argv.shift
            return missing_option_value(option) unless value

            line_length = Integer(value, exception: false)
            unless line_length && line_length.positive?
              @err.puts("--max-line-length must be a positive integer")
              print_usage(@err)
              return nil
            end

            options[:max_line_length] = line_length
          when "--profile"
            options[:profile] = true
          else
            @err.puts("unknown format option #{option}")
            print_usage(@err)
            return nil
          end
        else
          input_paths << option
        end
      end

      if options[:check] && options[:write]
        @err.puts("format options --check and --write cannot be combined")
        print_usage(@err)
        return nil
      end

      { options:, input_paths: }
    end

