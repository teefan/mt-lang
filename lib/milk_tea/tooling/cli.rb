    def format_size(bytes)
      return "0 B" if bytes.zero?

      units = %w[B KiB MiB GiB]
      exp = (Math.log(bytes) / Math.log(1024)).to_i
      exp = units.length - 1 if exp >= units.length
      format("%.1f %s", bytes.to_f / (1024**exp), units[exp])
    end

