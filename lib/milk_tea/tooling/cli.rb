    def extract_resolution_flags!
      locked = false
      frozen = false
      warnings_as_errors = false
      remaining = []

      @argv.each do |arg|
        if arg == "--locked"
          locked = true
        elsif arg == "--frozen"
          locked = true
          frozen = true
        elsif arg == "-Werror" || arg == "--warnings-as-errors"
          warnings_as_errors = true
        else
          remaining << arg
        end
      end

      @argv = remaining
      { locked:, frozen:, warnings_as_errors: }
    end

